# useful common patterns with strings

# joining lists into a string

greeting = ["The", "Lord", "is", "Great"]
new_sentence = " ".join(greeting)

print(new_sentence)

# Another one is reversing a list using [::-1]
salary = [10, 90, 82, 10, 82]
reversed_salary = salary[::-1]
print(reversed_salary)


